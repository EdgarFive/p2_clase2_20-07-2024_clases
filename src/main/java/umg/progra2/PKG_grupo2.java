package umg.progra2;

public class PKG_grupo2 {

    public double dosuno(double eebase, double eealtura){
        double areaa = (eebase * eealtura) / 2;

        return areaa;
    }


    public double dosdos(double eebase, double eealtura){
        double eevolumen = (eebase * eealtura) / 3;

        return eevolumen;
    }

}
